public class Kalkulator {
    public int tambah(int a, int b){
        return a + b;
    }
    
    public int kurang(int a, int b) {
        return a - b;
    }
    
    public int bagi(int a, int b) {
        return a / b;
    }
    
    public int kali(int a, int b) {
        return a * b;
    }
}
