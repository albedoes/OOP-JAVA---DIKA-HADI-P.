package com.mycompany.pertemuan4no1;

/**
 *
 * @author dikah
 */
public class Pertemuan4No1 {

      public static void main(String[] args) {
          
        int angka1 = 10;
        int angka2 = 3;

        System.out.println("Hasil Pertambahan A+B = " + (angka1 + angka2));
        
        System.out.println("Hasil Pengurangan A-B = " + (angka1 - angka2));
        
        System.out.println("Hasil Perkalian AxB = " + (angka1 * angka2));
        
        System.out.println("Hasil Pembagian A/B = " + (float)angka1 / angka2);       
        
    }
}